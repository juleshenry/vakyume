# Chapter 11 : Rotational dynamics
# 11-8 Moment of inertia
"""
I := moment of inertia
i := moment of inertia
m := mass
r := distance
"""
I = (m * r**2) / i

# 11-10 Parallel Axis Theorem
"""
Ih := moment of inertia about a parallel axis
ICM := moment of inertia about the centre of mass
M := mass of the object
h := distance from the centre of mass to the parallel axis
"""
Ih = ICM + M * h**2
