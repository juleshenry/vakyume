def safe_brentq(f, lo=1e-10, hi=1e10):
    """Adaptive brentq: tries multiple brackets with subdivision, returns all positive roots."""
    import math
    from scipy.optimize import brentq

    def _real_finite(v):
        """Return real part if finite, else None."""
        if isinstance(v, complex):
            if abs(v.imag) > 1e-10:
                return None
            v = v.real
        try:
            if math.isfinite(v):
                return float(v)
        except (TypeError, ValueError, OverflowError):
            pass
        return None

    def _real_f(x, _f=f):
        v = _f(x)
        if isinstance(v, complex):
            return v.real
        return float(v)

    brackets = [
        (lo, hi),
        (1e-10, 1e-3),
        (1e-3, 1.0),
        (0.1, 10.0),
        (0.01, 2.0),
        (0.5, 5.0),
        (1.0, 100.0),
        (1.0, 1e3),
        (1.0, 1e6),
        (1e-6, 1e6),
        (1e-6, 1e10),
        (-1e6, -1e-6),
        (-1e6, 1e6),
    ]
    roots = []
    for a, b in brackets:
        # Subdivide each bracket into sub-intervals to catch
        # non-monotone functions that cross zero in narrow windows.
        N = 50
        try:
            step = (b - a) / N
        except (OverflowError, ZeroDivisionError):
            continue
        for j in range(N):
            sub_a = a + j * step
            sub_b = a + (j + 1) * step
            try:
                fa = _real_finite(f(sub_a))
                fb = _real_finite(f(sub_b))
                if fa is None or fb is None:
                    continue
                if fa * fb < 0:
                    root = brentq(_real_f, sub_a, sub_b)
                    roots.append(root)
            except Exception:
                continue
    if not roots:
        raise ValueError("safe_brentq: no bracket found with sign change")
    # Deduplicate and return the largest positive root
    unique_roots = list(set(round(r, 10) for r in roots))
    positive = [r for r in unique_roots if r > 0]
    if positive:
        return max(positive)
    return max(unique_roots, key=abs)


