def attempt_repair_shard(
    ctx,
    family_name: str,
    broken_variant: str,
    trusted_variants: list,
    scores: dict,
    mismatches: dict,
    error: str = None,
    golden_test_cases: list = None,
):
    """Repair a single shard file using cascading strategies.

    Strategy order:
      1. Quick-fix (postprocessing + return wrapping)
      2. SymPy algebraic solve
      3. Numerical brentq solve
      4. LLM-assisted code generation with retry loop
    """
    context = _extract_shard_context(ctx, family_name, broken_variant)
    if context is None:
        return {"updated": False}

    shard_path, shard_file, shard_code, pyeqn, header, eqn_suffix = context
    method_name = f"eqn_{eqn_suffix}__{broken_variant}"

    print(f"\n |- Repairing shard: {shard_file} in family {family_name}")

    # Strategy 1: Quick-fix pass
    result = _try_quick_fix(
        shard_code,
        shard_path,
        method_name,
        golden_test_cases,
        broken_variant,
        shard_file,
    )
    if result is not None:
        return result

    # Strategy 2 & 3: SymPy algebraic / brentq pass
    sympy_result = _sympy_fallback(
        pyeqn,
        broken_variant,
        eqn_suffix,
        header,
        shard_path,
        golden_test_cases or [],
    )
    if sympy_result.get("updated"):
        return sympy_result

    # Strategy 4: LLM repair loop
    return _run_llm_repair_loop(
        pyeqn,
        broken_variant,
        header,
        shard_path,
        shard_file,
        eqn_suffix,
        golden_test_cases or [],
    )
