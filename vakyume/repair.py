def _extract_shard_context(ctx, family_name: str, broken_variant: str):
    """Read the broken shard and extract its pyeqn, header, and path.

    Returns ``(shard_path, shard_file, shard_code, pyeqn, header, eqn_suffix)``
    or ``None`` if the shard can't be found.
    """
    family_dir = os.path.join(ctx.shards_dir, family_name)
    eqn_suffix = family_name.split("_eqn_")[1]
    shard_path = _find_shard_file(family_dir, eqn_suffix, broken_variant)

    if not shard_path:
        print(f"  [Repair] Shard for '{broken_variant}' not found in {family_name}")
        return None

    shard_file = os.path.basename(shard_path)

    with open(shard_path, "r") as f:
        shard_code = f.read()

    pyeqn_match = re.search(r"# \[\.pyeqn\] (.*)", shard_code)
    pyeqn = pyeqn_match.group(1).strip() if pyeqn_match else ""
    pyeqn = re.sub(
        r"^(?:Solve\s+for\s+\w+\s+in\s+the\s+equation\s+)",
        "",
        pyeqn,
        flags=re.IGNORECASE,
    ).strip()

    # Extract the function header from the broken shard
    header_match = re.search(
        r"(def eqn_.*?\*\*kwargs\s*,?\s*\)):", shard_code, re.DOTALL
    )
    header = (
        re.sub(r"\s+", " ", header_match.group(1)).strip() + ":" if header_match else ""
    )

    # Validate header: target var must NOT be a parameter, other vars must be present
    header = _validate_or_regen_header(header, pyeqn, broken_variant, eqn_suffix)

    return shard_path, shard_file, shard_code, pyeqn, header, eqn_suffix


def _validate_or_regen_header(
    header: str, pyeqn: str, broken_variant: str, eqn_suffix: str
) -> str:
    """Ensure the function header is correct, regenerating it if needed."""
    if not header or not pyeqn:
        return header

    all_tokens = set(re.findall(r"\b[a-zA-Z_]\w*\b", pyeqn))
    eq_vars = sorted(
        t for t in all_tokens if t not in MATH_FUNC_NAMES_WITH_SELF and not t.isdigit()
    )
    other_vars = [v for v in eq_vars if v != broken_variant]

    param_section = header.split("(", 1)[1] if "(" in header else ""
    param_tokens = set(re.findall(r"\b[a-zA-Z_]\w*\b", param_section))
    param_tokens -= {"self", "kwargs", "float", "int"}

    needs_regen = broken_variant in param_tokens
    if not needs_regen:
        for ov in other_vars:
            if ov not in param_tokens:
                needs_regen = True
                break

    if needs_regen:
        func_name = f"eqn_{eqn_suffix}__{broken_variant}"
        params = ", ".join(f"{v}: float" for v in other_vars)
        header = f"def {func_name}(self, {params}, **kwargs):"
        print(f"  [Repair] Regenerated header: {header}")

    return header


def _try_quick_fix(
    shard_code: str,
    shard_path: str,
    method_name: str,
    golden_test_cases: list,
    broken_variant: str,
    shard_file: str,
) -> dict | None:
    """Try to fix the existing shard by applying postprocessing + return wrapping.

    Returns ``{"updated": True}`` on success, ``None`` to fall through.
    """
    qf_code = shard_code
    def_idx = qf_code.find("\ndef ")
    if def_idx == -1:
        def_idx = qf_code.find("def ")
    if def_idx < 0:
        return None

    qf_body = qf_code[def_idx:].lstrip("\n")
    qf_body = _BARE_RETURN_RE.sub(r"return [\1]", qf_body)
    qf_body = _postprocess_code(qf_body)
    qf_full = SHARD_IMPORT_HEADER + qf_body + "\n"

    if qf_full == shard_code:
        return None  # postprocessing changed nothing

    try:
        ast.parse(qf_full)
    except SyntaxError:
        print(f"  [Repair] Quick-fix pass had syntax errors — proceeding to LLM")
        return None

    ok = _try_write_and_validate(
        qf_full,
        shard_path,
        method_name,
        golden_test_cases or [],
        broken_variant,
        label="Quick-fix",
    )
    if ok:
        print(f"  [Repair] Quick-fix pass succeeded for {shard_file} — skipped LLM")
        return {"updated": True}

    print(f"  [Repair] Quick-fix pass did not pass golden tests — proceeding to LLM")
    return None


def _sanitize_llm_code(
    raw_code: str,
    method_name: str,
    header: str,
    pyeqn: str,
) -> str:
    """Clean up LLM-generated code: force correct header, strip imports, wrap returns."""
    code_text = extract_code(raw_code, target_name=method_name)
    if not code_text.strip():
        code_text = extract_code(raw_code)
    if not code_text.strip():
        return ""

    # Force the correct header — don't trust what the LLM wrote
    any_def = re.search(r"def\s+\w+\s*\(.*?\)\s*(?:->.*?)?:", code_text, re.DOTALL)
    if any_def and header:
        code_text = code_text.replace(any_def.group(0), header)
    elif any_def:
        code_text = code_text.replace(
            any_def.group(0),
            re.sub(r"def\s+\w+", f"def {method_name}", any_def.group(0)),
        )

    # Strip stray imports
    body_lines = []
    for line in code_text.splitlines():
        s = line.strip()
        if s.startswith("import ") or s.startswith("from "):
            continue
        body_lines.append(line)
    code_text = "\n".join(body_lines)

    # Inject pyeqn comment if missing
    if "# [.pyeqn]" not in code_text and pyeqn:
        code_text = re.sub(
            r"(def\s+\w+\(.*?\):)",
            rf"\1\n    # [.pyeqn] {pyeqn}",
            code_text,
            count=1,
        )

    # Wrap bare scalar returns in a list
    code_text = _BARE_RETURN_RE.sub(r"return [\1]", code_text)
    code_text = _postprocess_code(code_text)

    return code_text


def _build_repair_prompt(
    pyeqn: str, broken_variant: str, golden_test_cases: list, header: str
):
    """Build the initial LLM prompt for repairing a shard."""
    user_prompt = f"{pyeqn}\nSolve for {broken_variant}.\n"
    if golden_test_cases:
        tc = golden_test_cases[0]
        inputs_str = ", ".join(
            f"{k}={v}" for k, v in sorted(tc.get("inputs", {}).items())
        )
        user_prompt += (
            f"Test: {inputs_str} -> {broken_variant}={tc.get('expected', '?')}\n"
        )
    user_prompt += f"\n{header}\n"
    return user_prompt


_LLM_SYSTEM_PROMPT = (
    "Write a Python function. Output ONLY code. No markdown. No comments.\n"
    "Use log/sqrt/exp from cmath. Return [value]. Keep the signature."
)

MAX_SYNTAX_RETRIES = 3
MAX_SEMANTIC_RETRIES = 3


def _run_llm_repair_loop(
    pyeqn: str,
    broken_variant: str,
    header: str,
    shard_path: str,
    shard_file: str,
    eqn_suffix: str,
    golden_test_cases: list,
) -> dict:
    """Run the LLM repair loop with syntax and semantic retries.

    Returns ``{"updated": True}`` on success, ``{"updated": False}`` otherwise.
    """
    method_name = f"eqn_{eqn_suffix}__{broken_variant}"
    user_prompt = _build_repair_prompt(pyeqn, broken_variant, golden_test_cases, header)
    raw = ask_llm(_LLM_SYSTEM_PROMPT, user_prompt, stream=False)

    if not raw:
        print(f"  [Repair] LLM returned empty response")
        return {"updated": False}

    syntax_failures = 0
    semantic_failures = 0
    max_total_attempts = MAX_SYNTAX_RETRIES + MAX_SEMANTIC_RETRIES

    for attempt in range(1, max_total_attempts + 1):
        code_text = _sanitize_llm_code(raw, method_name, header, pyeqn)
        if not code_text:
            print(f"  [Repair] extract_code returned empty")
            return {"updated": False}

        full_code = SHARD_IMPORT_HEADER + code_text + "\n"

        try:
            ast.parse(full_code)

            # Semantic validation against golden tuples
            ok = _try_write_and_validate(
                full_code,
                shard_path,
                method_name,
                golden_test_cases or [],
                broken_variant,
                label="LLM",
            )

            if not ok:
                semantic_failures += 1
                if semantic_failures < MAX_SEMANTIC_RETRIES:
                    print(
                        f"  [Repair] Semantically wrong (semantic attempt "
                        f"{semantic_failures}/{MAX_SEMANTIC_RETRIES}), retrying..."
                    )
                    retry_prompt = _build_repair_prompt(
                        pyeqn, broken_variant, golden_test_cases, header
                    )
                    retry_prompt = (
                        retry_prompt.rstrip("\n")
                        + f"\nWrong code:\n{code_text}\nFix it.\n"
                    )
                    raw = ask_llm(_LLM_SYSTEM_PROMPT, retry_prompt, stream=False)
                    if not raw:
                        print(f"  [Repair] LLM returned empty on semantic-fix retry")
                        return {"updated": False}
                    continue
                else:
                    print(
                        f"  [Repair] Semantic validation failed after "
                        f"{MAX_SEMANTIC_RETRIES} semantic attempts"
                    )
                    return {"updated": False}

            with open(shard_path, "w") as f:
                f.write(full_code)
            print(f"  [Repair] Successfully wrote LLM-repaired shard: {shard_file}")
            return {"updated": True}

        except SyntaxError as e:
            syntax_failures += 1
            if syntax_failures < MAX_SYNTAX_RETRIES:
                print(
                    f"  [Repair] Syntax error (syntax attempt "
                    f"{syntax_failures}/{MAX_SYNTAX_RETRIES}): {e}"
                )
                print(f"  [Repair] Feeding code back to LLM for correction...")
                fix_prompt = (
                    f"{pyeqn}\nSolve for {broken_variant}.\n"
                    f"Syntax error: {e}\nFix it. Output ONLY the function.\n\n{header}\n"
                )
                raw = ask_llm(_LLM_SYSTEM_PROMPT, fix_prompt, stream=False)
                if not raw:
                    print(f"  [Repair] LLM returned empty on syntax-fix retry")
                    return {"updated": False}
            else:
                print(
                    f"  [Repair] LLM produced invalid syntax after "
                    f"{MAX_SYNTAX_RETRIES} syntax attempts"
                )
                return {"updated": False}

    return {"updated": False}


