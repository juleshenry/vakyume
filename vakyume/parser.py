"""Equation parsing and shard generation.

Reads Python-like equation definitions from ``notes/*.py`` files and generates
individual solver shards using SymPy for algebraic isolation.
"""

import logging
import os
import py_compile
import re

import timeout_decorator
from sympy import Symbol, solve, sympify

from .config import (
    TAB,
    UnsolvedException,
    MAX_COMP_TIME_SECONDS,
    FUNKTORZ,
    SHARD_IMPORT_HEADER,
)

logger = logging.getLogger(__name__)


def case_safe_name(name: str) -> str:
    """Convert a variable name to a filesystem-safe form.

    Each uppercase letter ``X`` is replaced by ``X_cap`` so that
    case-insensitive filesystems never collide – even for names that
    differ only in casing pattern (e.g. ``aA`` vs ``Aa`` vs ``AA``).

    Examples::

        >>> case_safe_name("aa")
        'aa'
        >>> case_safe_name("aA")
        'aA_cap'
        >>> case_safe_name("Aa")
        'A_capa'
        >>> case_safe_name("AA")
        'A_capA_cap'
        >>> case_safe_name("T")
        'T_cap'
        >>> case_safe_name("P_1")
        'P_cap_1'
    """
    parts: list[str] = []
    for c in name:
        if c.isupper():
            parts.append(f"{c}_cap")
        else:
            parts.append(c)
    return "".join(parts)


def case_unsafe_name(safe: str) -> str:
    """Reverse :func:`case_safe_name` – recover the original variable name.

    Strips every ``_cap`` that immediately follows an uppercase letter,
    restoring the original mixed-case variable name.

    Examples::

        >>> case_unsafe_name("aa")
        'aa'
        >>> case_unsafe_name("aA_cap")
        'aA'
        >>> case_unsafe_name("A_capa")
        'Aa'
        >>> case_unsafe_name("A_capA_cap")
        'AA'
        >>> case_unsafe_name("T_cap")
        'T'
        >>> case_unsafe_name("P_cap_1")
        'P_1'
    """
    return re.sub(r"([A-Z])_cap", r"\1", safe)


def _find_existing_shard(family_dir: str, eqn_number: str, token: str) -> str | None:
    """Check if a shard file already exists for *token*, regardless of naming convention.

    Returns the full path if found, None otherwise.
    """
    method_name = f"eqn_{eqn_number}__{token}"
    # Fast check: raw name first
    raw_path = os.path.join(family_dir, f"{method_name}.py")
    if os.path.exists(raw_path):
        return raw_path
    # Scan for any file that defines this function
    try:
        for fname in os.listdir(family_dir):
            if not fname.endswith(".py") or "__" not in fname:
                continue
            fpath = os.path.join(family_dir, fname)
            with open(fpath, "r") as fh:
                for line in fh:
                    if line.strip().startswith(f"def {method_name}("):
                        return fpath
    except OSError:
        pass
    return None


class Solver:
    """SymPy-based equation solver with tokenizer and normal-form builder."""

    def __init__(self):
        self.funktors = FUNKTORZ

    @timeout_decorator.timeout(
        MAX_COMP_TIME_SECONDS,
        timeout_exception=timeout_decorator.timeout_decorator.TimeoutError,
    )
    def get_solns_vanilla_nf(
        self, nf: str, symb: Symbol, tokens: list[str] | None = None
    ):
        try:
            local_dict = {t: Symbol(t) for t in tokens} if tokens else {}
            expr = sympify(nf, locals=local_dict)  # type: ignore
            solns = solve(expr, symb)
            if not solns:
                raise UnsolvedException("Sympy solve returned empty")
            return solns
        except Exception as e:
            raise UnsolvedException(f"Sympy solve failed: {e}")

    def tokenize(self, eqn):
        malos = {"ln", "log"}
        for m in malos:
            eqn = f"{m}".join([o.strip() for o in eqn.split(m)])

        eqn = eqn.split("#")[0]
        dilated = ""
        for i, f in enumerate(eqn):
            if (
                0 < i < len(eqn) - 1
                and eqn[i + 1] != "*"
                and eqn[i - 1] != "*"
                and f in self.funktors
            ):
                dilated += f" {f} "
            else:
                dilated += f
        return dilated.split()

    def get_tokes(self, eqn):
        tokes = set()
        malos = {"ln", "log"}
        for t in self.tokenize(eqn):
            clean = t.strip().replace("(", "").replace(")", "").split("**")[0].strip()
            if clean.isidentifier() and clean not in malos:
                tokes.add(clean)
        return sorted(list(tokes))

    def make_normal_form(self, eqn):
        parts = eqn.split("=")
        if len(parts) != 2:
            return None
        nf = f"({parts[1].split('#')[0].strip()}) - ({parts[0].strip()})"
        return nf.replace("ln(", "log(").replace("ln (", "log(")

    def sympy_failover(self):
        code = [
            f"{TAB}# Placeholder for numerical solver",
            f'{TAB}raise UnsolvedException("Pending LLM/Manual Repair")',
        ]
        return "\n".join(code)


def shard_from_chapters(ctx, overwrite_existing=False):
    """Parse equation notes and generate individual solver shard files.

    Parameters
    ----------
    ctx : PipelineContext
        Pipeline context with ``notes_dir`` and ``shards_dir`` paths.
    overwrite_existing : bool, optional
        Whether to overwrite existing shard files. Defaults to False.
    """
    logger.debug("shard_from_chapters: ctx.notes_dir=%s", ctx.notes_dir)
    solver = Solver()

    if not os.path.exists(ctx.notes_dir):
        print(f"Notes directory not found: {ctx.notes_dir}")
        return

    chapter_files = sorted(os.listdir(ctx.notes_dir))
    created_count = 0

    to_process = []
    for chapter_file in chapter_files:
        if chapter_file.endswith(".py") and not chapter_file.startswith("__"):
            to_process.append(chapter_file)

    if not to_process:
        logger.debug("shard_from_chapters: No chapters to process")
        return

    import_header = SHARD_IMPORT_HEADER

    for chapter_file in to_process:
        chapter_path = os.path.join(ctx.notes_dir, chapter_file)
        base_name = os.path.splitext(chapter_file)[0]
        parts = base_name.split("_")
        if parts[0].isdigit():
            parts = parts[1:]
        class_name = "".join(p.capitalize() for p in parts)

        with open(chapter_path, "r") as f:
            lines = f.readlines()

        eqn_number = ""
        var_notes: list[str] = []
        for line in lines:
            stripped = line.strip()

            # Collect variable annotations from := lines
            if ":=" in stripped:
                note = stripped.replace('"""', "").strip()
                if ":=" in note:
                    var_notes.append(note)

            if x := re.findall(r"\d{1,2}-\d{1,2}\w*", line):
                eqn_number = x[0].replace("-", "_")
                var_notes = []

            if (
                " = " in line
                and not stripped.startswith("#")
                and not stripped.startswith('"""')
            ):
                tokes = solver.get_tokes(line)
                nf = solver.make_normal_form(line)
                if not nf:
                    continue

                family_name = f"{class_name}_eqn_{eqn_number}"
                family_dir = os.path.join(ctx.shards_dir, family_name)
                if not os.path.exists(family_dir):
                    os.makedirs(family_dir)

                for token in tokes:
                    method_name = f"eqn_{eqn_number}__{token}"
                    shard_name = f"eqn_{eqn_number}__{token}.py"
                    shard_path = os.path.join(family_dir, shard_name)
                    # Also check for existing shard under old naming conventions
                    existing = _find_existing_shard(family_dir, eqn_number, token)
                    if existing and not overwrite_existing:
                        continue
                    if existing and overwrite_existing:
                        shard_path = existing  # overwrite in-place

                    created_count += 1
                    shard_content = import_header
                    other_args = [t for t in tokes if t != token]
                    args_str = ", ".join(f"{t}: float" for t in other_args)
                    if args_str:
                        args_str = f", {args_str}"
                    header = f"def {method_name}(self{args_str}, **kwargs):"
                    shard_content += header + "\n"
                    shard_content += f"{TAB}# [.pyeqn] {line.strip()}\n"

                    try:
                        solns = solver.get_solns_vanilla_nf(
                            nf, Symbol(token), tokens=tokes
                        )
                        if solns:
                            shard_content += f"{TAB}result = []\n"
                            for soln in solns:
                                shard_content += f"{TAB}{token} = {soln}\n"
                                shard_content += f"{TAB}result.append({token})\n"
                            shard_content += f"{TAB}return result\n"
                        else:
                            shard_content += solver.sympy_failover() + "\n"
                    except Exception:
                        shard_content += solver.sympy_failover() + "\n"

                    with open(shard_path, "w") as sf:
                        sf.write(shard_content)

                    try:
                        py_compile.compile(shard_path, doraise=True)
                    except py_compile.PyCompileError as e:
                        print(f"  [py_compile] FAILED for {shard_path}: {e}")

                # Main shard for the equation (Class and kwasak entry point)
                main_shard_path = os.path.join(family_dir, f"eqn_{eqn_number}.py")
                if not os.path.exists(main_shard_path):
                    with open(main_shard_path, "w") as sf:
                        sf.write(import_header)
                        sf.write("from vakyume.kwasak import kwasak\n")
                        for token in tokes:
                            method_name = f"eqn_{eqn_number}__{token}"
                            file_name_no_ext = f"eqn_{eqn_number}__{token}"
                            sf.write(f"from .{file_name_no_ext} import {method_name}\n")
                        sf.write(f"\nclass {class_name}:\n")
                        for token in tokes:
                            method_name = f"eqn_{eqn_number}__{token}"
                            sf.write(f"{TAB}{method_name} = {method_name}\n")
                        sf.write(f"\n{TAB}@kwasak\n")
                        tokes_str = ", ".join(f"{t}=None" for t in tokes)
                        sf.write(f"{TAB}def eqn_{eqn_number}(self, {tokes_str}):\n")
                        if var_notes:
                            sf.write(f'{TAB * 2}"""\n')
                            for note in var_notes:
                                sf.write(f"{TAB * 2}{note}\n")
                            sf.write(f'{TAB * 2}"""\n')
                        sf.write(f"{TAB * 2}return\n")

    print(f"Scraped {created_count} new shards.")
