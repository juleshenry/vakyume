"""Vakyume C++ code generator: transpiles Python solver libraries to compiled C++.

Two-tier strategy:
  1. **Deterministic AST transpiler** — walks the Python AST of each solver
     method and emits equivalent C++.  Handles arithmetic, ``log``, ``sqrt``,
     ``exp``, ``pow``, list construction, and the ``result.append(…)`` pattern
     used by every Vakyume shard.
  2. **LLM fallback** (Phi-3 via Ollama) — used only when the AST transpiler
     cannot handle a construct (e.g. SymPy symbolic calls).
"""

import ast
import subprocess
import os
import re
import textwrap


# ── AST helpers ──────────────────────────────────────────────────────────────

# Python names -> C++ equivalents
_MATH_FUNCS = {
    "log": "std::log",
    "sqrt": "std::sqrt",
    "exp": "std::exp",
    "pow": "std::pow",
    "abs": "std::abs",
    "LambertW": "_lambertW",
}

# C++ implementation of the Lambert W₀ function (principal branch) via
# Halley's iteration.  Emitted once at the top of the generated file.
_LAMBERTW_IMPL = """\
// Lambert W₀ (principal branch) via Halley iteration
inline double _lambertW(double x) {
    if (x == 0.0) return 0.0;
    double w = (x > 1.0) ? std::log(x) - std::log(std::log(x)) : 0.5;
    for (int i = 0; i < 50; ++i) {
        double ew = std::exp(w);
        double wew = w * ew;
        double f  = wew - x;
        double fp = ew * (w + 1.0);
        double d  = f / (fp - (w + 2.0) * f / (2.0 * (w + 1.0)));
        w -= d;
        if (std::abs(d) < 1e-15) break;
    }
    return w;
}
"""

_MATH_CONSTS = {
    "e": "M_E",
    "pi": "M_PI",
}


class _CppTranspileError(Exception):
    """Raised when the deterministic transpiler cannot handle a construct."""


class _ExprTranspiler(ast.NodeVisitor):
    """Convert a Python expression AST node to a C++ expression string."""

    def __init__(self, complex_mode=False):
        self.complex_mode = complex_mode

    def visit_Constant(self, node):
        if isinstance(node.value, (int, float)):
            v = repr(node.value)
            # Ensure floating-point literal
            if isinstance(node.value, int):
                v += ".0"
            return v
        raise _CppTranspileError(f"unsupported constant: {node.value!r}")

    def visit_Name(self, node):
        if node.id in _MATH_CONSTS:
            return _MATH_CONSTS[node.id]
        # SymPy imaginary unit
        if node.id == "I":
            return "std::complex<double>(0.0, 1.0)"
        return node.id

    def visit_UnaryOp(self, node):
        operand = self.visit(node.operand)
        if isinstance(node.op, ast.USub):
            return f"(-{operand})"
        if isinstance(node.op, ast.UAdd):
            return f"(+{operand})"
        raise _CppTranspileError(f"unsupported unary op: {type(node.op).__name__}")

    def visit_BinOp(self, node):
        left = self.visit(node.left)
        right = self.visit(node.right)
        ops = {
            ast.Add: "+",
            ast.Sub: "-",
            ast.Mult: "*",
            ast.Div: "/",
        }
        op_type = type(node.op)
        if op_type == ast.Pow:
            return f"std::pow({left}, {right})"
        if op_type in ops:
            return f"({left} {ops[op_type]} {right})"
        raise _CppTranspileError(f"unsupported binop: {op_type.__name__}")

    def visit_Call(self, node):
        # Plain function call: log(x), sqrt(x), etc.
        if isinstance(node.func, ast.Name):
            fname = node.func.id
            if fname in _MATH_FUNCS:
                args = ", ".join(self.visit(a) for a in node.args)
                return f"{_MATH_FUNCS[fname]}({args})"
            raise _CppTranspileError(f"unsupported function: {fname}")

        # Attribute call: e.g. result.append(x) — handled at statement level
        raise _CppTranspileError("unsupported call expression")

    def visit_Attribute(self, node):
        # e.g. math.log — we treat the attribute name directly
        if isinstance(node.value, ast.Name):
            combined = f"{node.value.id}.{node.attr}"
            # math.log -> std::log, etc.
            if node.attr in _MATH_FUNCS:
                return _MATH_FUNCS[node.attr]
            if node.attr in _MATH_CONSTS:
                return _MATH_CONSTS[node.attr]
        raise _CppTranspileError(f"unsupported attribute: {ast.dump(node)}")

    def visit_List(self, node):
        # [expr, ...] — used in result = [val]
        elts = ", ".join(self.visit(e) for e in node.elts)
        return f"{{{elts}}}"

    def visit_Subscript(self, node):
        value = self.visit(node.value)
        sl = self.visit(node.slice)
        return f"{value}[{sl}]"

    # fallback
    def generic_visit(self, node):
        raise _CppTranspileError(f"unsupported AST node: {type(node).__name__}")


def _transpile_expr(node):
    """Transpile a single Python expression node to C++."""
    return _ExprTranspiler().visit(node)


def _method_uses_complex(source):
    """Return True if the Python source uses the imaginary unit ``I``."""
    tree = ast.parse(textwrap.dedent(source))
    for node in ast.walk(tree):
        if isinstance(node, ast.Name) and node.id == "I":
            return True
    return False


def _transpile_method(class_name, method_name, source):
    """
    Attempt a deterministic Python→C++ conversion of a solver method.

    Returns the C++ function string, or raises ``_CppTranspileError`` if
    the method contains constructs the transpiler cannot handle.
    """
    tree = ast.parse(textwrap.dedent(source))
    func_node = None
    for node in ast.walk(tree):
        if isinstance(node, ast.FunctionDef):
            func_node = node
            break
    if func_node is None:
        raise _CppTranspileError("no function found")

    full_name = f"{class_name}_{method_name}"

    # Detect whether this method produces complex-valued solutions
    complex_mode = _method_uses_complex(source)
    val_type = "std::complex<double>" if complex_mode else "double"
    vec_type = f"std::vector<{val_type}>"

    # Build parameter list (skip **kwargs, self, cls)
    params = []
    for arg in func_node.args.args:
        name = arg.arg
        if name in ("self", "cls"):
            continue
        params.append(f"double {name}")
    # Skip **kwargs (func_node.args.kwarg)

    sig = f"{vec_type} {full_name}({', '.join(params)})"

    body_lines = []
    body_lines.append(f"    {vec_type} result;")

    tx = _ExprTranspiler(complex_mode=complex_mode)

    # Track declared variables to avoid redefinitions
    declared_vars = set()

    for stmt in func_node.body:
        # Skip: docstrings, comments (ast doesn't keep comments, but docstrings are Expr(Constant(str)))
        if (
            isinstance(stmt, ast.Expr)
            and isinstance(stmt.value, ast.Constant)
            and isinstance(stmt.value.value, str)
        ):
            body_lines.append(f"    // {stmt.value.value.strip()}")
            continue

        # result = []
        if (
            isinstance(stmt, ast.Assign)
            and len(stmt.targets) == 1
            and isinstance(stmt.targets[0], ast.Name)
            and stmt.targets[0].id == "result"
            and isinstance(stmt.value, ast.List)
            and len(stmt.value.elts) == 0
        ):
            # Already emitted above
            continue

        # result.append(expr)
        if (
            isinstance(stmt, ast.Expr)
            and isinstance(stmt.value, ast.Call)
            and isinstance(stmt.value.func, ast.Attribute)
            and isinstance(stmt.value.func.value, ast.Name)
            and stmt.value.func.value.id == "result"
            and stmt.value.func.attr == "append"
        ):
            arg_expr = tx.visit(stmt.value.args[0])
            body_lines.append(f"    result.push_back({arg_expr});")
            continue

        # return result
        if isinstance(stmt, ast.Return):
            if stmt.value is None:
                body_lines.append("    return result;")
            elif isinstance(stmt.value, ast.Name) and stmt.value.id == "result":
                body_lines.append("    return result;")
            else:
                val = tx.visit(stmt.value)
                # If we're returning a single value but the function return type is std::vector,
                # wrap it in an initializer list.
                if not (val.startswith("{") and val.endswith("}")):
                    val = f"{{ {val} }}"
                body_lines.append(f"    return {val};")
            continue

        # Simple assignment: var = expr
        if (
            isinstance(stmt, ast.Assign)
            and len(stmt.targets) == 1
            and isinstance(stmt.targets[0], ast.Name)
        ):
            var_name = stmt.targets[0].id
            val = tx.visit(stmt.value)
            if var_name not in declared_vars:
                body_lines.append(f"    {val_type} {var_name} = {val};")
                declared_vars.add(var_name)
            else:
                body_lines.append(f"    {var_name} = {val};")
            continue

        raise _CppTranspileError(f"unsupported statement: {type(stmt).__name__}")

    # Only append a trailing return if the body doesn't already end with one
    if not body_lines or not body_lines[-1].strip().startswith("return"):
        body_lines.append("    return result;")

    return sig + " {\n" + "\n".join(body_lines) + "\n}"


# ── LLM fallback ────────────────────────────────────────────────────────────


def convert_with_phi3(class_name, method_name, python_code):
    full_func_name = f"{class_name}_{method_name}"
    prompt = f"""Convert this Python static method from class '{class_name}' to a C++ function.
Function name: {full_func_name}
Return type: std::vector<double> (if all real) or std::vector<std::complex<double>> (if complex I is used).
Math: Use std::log, std::sqrt, std::exp, std::pow from <cmath>.
Complex: Use std::complex<double> and std::complex<double>(0, 1) for I.
Floating point: Use double for all calculations. Use .0 suffix for integers (e.g. 530.0 instead of 530).
Return a vector containing the results.
DO NOT add any logic that is not in the Python code. DO NOT add conversion factors.
ONLY output the C++ code, no preamble, no markdown blocks, no explanation.

Python:
{python_code}
"""
    try:
        result = subprocess.run(
            ["ollama", "run", "phi3", prompt],
            capture_output=True,
            text=True,
            check=True,
        )
        code = result.stdout.strip()
        # Clean up markdown
        code = re.sub(r"```cpp\n?", "", code)
        code = re.sub(r"```\n?", "", code)
        code = code.strip("`").strip()
        # Find the start of the C++ code
        if "std::vector" in code:
            code = code[code.find("std::vector") :]
        return code
    except Exception as e:
        return f"// Error converting {full_func_name}: {e}"


# ── Conversion dispatch ─────────────────────────────────────────────────────


def convert_method(class_name, method_name, source):
    """Try deterministic transpilation first; fall back to LLM, then stub."""
    try:
        return _transpile_method(class_name, method_name, source)
    except _CppTranspileError:
        return convert_with_phi3(class_name, method_name, source)


def _make_stub(class_name, method_name, arg_names):
    """Generate a C++ stub that throws at runtime for untranspilable methods."""
    full_name = f"{class_name}_{method_name}"
    real_args = [a for a in arg_names if a not in ("self", "cls")]
    params = ", ".join(f"double {a}" for a in real_args)
    return (
        f"std::vector<double> {full_name}({params}) {{\n"
        f'    throw std::runtime_error("{full_name}: requires numerical solver (not transpilable)");\n'
        f"}}"
    )


# ── Public helpers ───────────────────────────────────────────────────────────


def get_methods_from_file(file_path):
    with open(file_path, "r") as f:
        tree = ast.parse(f.read())

    classes_methods = {}
    for node in ast.walk(tree):
        if isinstance(node, ast.ClassDef):
            class_name = node.name
            methods = []
            for item in node.body:
                if isinstance(item, ast.FunctionDef):
                    if "__" in item.name and not item.name.startswith("__"):
                        methods.append(item)
            if methods:
                classes_methods[class_name] = methods
    return classes_methods


def get_method_source(file_path, method_node):
    with open(file_path, "r") as f:
        lines = f.readlines()
    return "".join(lines[method_node.lineno - 1 : method_node.end_lineno])


# ── Main entry point ────────────────────────────────────────────────────────


def _collect_source_files(project_dir, input_file=None):
    """Resolve Python source file(s) to convert to C++.

    Returns a list of absolute file paths.  Prefers:
      1. An explicit *input_file*.
      2. A monolithic ``certified.py`` (or legacy variants) in *project_dir*.
      3. The modular ``py/`` package produced by ``reconstruct``.
    """
    if input_file:
        path = (
            input_file
            if os.path.isabs(input_file)
            else os.path.join(project_dir, input_file)
        )
        if not os.path.exists(path):
            return []
        return [path]

    # Monolithic certified file (preferred)
    for name in ("certified.py", "vakyume_lib.py", "vakyume_certified.py"):
        path = os.path.join(project_dir, name)
        if os.path.exists(path):
            return [path]

    # Modular py/ package fallback
    py_dir = os.path.join(project_dir, "py")
    if os.path.isdir(py_dir):
        skip = {"__init__.py", "harmony_checks.py"}
        files = sorted(
            os.path.join(py_dir, f)
            for f in os.listdir(py_dir)
            if f.endswith(".py") and f not in skip
        )
        if files:
            return files

    return []


def main(project_dir=".", input_file=None):
    project_dir = os.path.abspath(project_dir)

    source_files = _collect_source_files(project_dir, input_file)

    if not source_files:
        # Show a helpful error pointing to what we looked for
        py_dir = os.path.join(project_dir, "py")
        print(
            f"Error: No Python source found.\n"
            f"  Looked for: certified.py, vakyume_lib.py, vakyume_certified.py\n"
            f"  Also checked: {py_dir}/"
        )
        return

    # ── Output directories ───────────────────────────────────────────────
    cpp_dir = os.path.join(project_dir, "cpp")
    bin_dir = os.path.join(project_dir, "bin")
    os.makedirs(cpp_dir, exist_ok=True)
    os.makedirs(bin_dir, exist_ok=True)

    test_binary_path = os.path.join(bin_dir, "vacuum_theory_binary")

    print(f"C++ source: {', '.join(source_files)}")

    # ── Collect methods from all source files ────────────────────────────
    all_tasks = []
    for src_file in source_files:
        classes_methods = get_methods_from_file(src_file)
        for class_name, methods in classes_methods.items():
            for method in methods:
                source = get_method_source(src_file, method)
                all_tasks.append(
                    (
                        class_name,
                        method.name,
                        source,
                        [arg.arg for arg in method.args.args],
                    )
                )

    # Set a limit if you want to test on a subset first, e.g., limit = 10
    limit = None
    tasks_to_run = all_tasks[:limit] if limit else all_tasks

    print(f"Converting {len(tasks_to_run)} functions to C++...")

    # First pass: deterministic transpiler (fast, no LLM).
    # Methods that can't be transpiled get runtime-error stubs.
    cpp_functions = [None] * len(tasks_to_run)
    stubbed = []

    for i, (class_name, method_name, source, args) in enumerate(tasks_to_run):
        try:
            cpp_functions[i] = _transpile_method(class_name, method_name, source)
        except _CppTranspileError:
            cpp_functions[i] = _make_stub(class_name, method_name, args)
            stubbed.append(f"{class_name}.{method_name}")

    det_count = len(tasks_to_run) - len(stubbed)
    if stubbed:
        print(
            f"  {det_count} converted deterministically, "
            f"{len(stubbed)} stubbed (not transpilable):"
        )
        for name in stubbed:
            print(f"    - {name}")
    else:
        print(f"  All {len(tasks_to_run)} converted deterministically.")

    # Check whether any generated function uses _lambertW
    all_code = "\n".join(f for f in cpp_functions if f)
    needs_lambertw = "_lambertW" in all_code

    # Also check per-class need for lambertW
    class_needs_lambertw: dict[str, bool] = {}

    # ── Group functions by class for per-header output ────────────────────
    class_funcs: dict[str, list[str]] = {}
    for (class_name, method_name, source, args), cpp_func in zip(
        tasks_to_run, cpp_functions
    ):
        if cpp_func:
            if class_name not in class_funcs:
                class_funcs[class_name] = []
            class_funcs[class_name].append(cpp_func)

    for class_name, funcs in class_funcs.items():
        class_needs_lambertw[class_name] = any("_lambertW" in f for f in funcs)

    # Common header preamble
    _header_preamble = (
        "#include <vector>\n"
        "#include <cmath>\n"
        "#include <complex>\n"
        "#include <stdexcept>\n"
        "\n"
        "using namespace std::complex_literals;\n"
    )

    # Write shared lambertw header if needed
    if needs_lambertw:
        lambertw_path = os.path.join(cpp_dir, "lambertw.hpp")
        with open(lambertw_path, "w") as lf:
            lf.write("#ifndef VAKYUME_LAMBERTW_HPP\n#define VAKYUME_LAMBERTW_HPP\n\n")
            lf.write("#include <cmath>\n\n")
            lf.write(_LAMBERTW_IMPL + "\n")
            lf.write("#endif // VAKYUME_LAMBERTW_HPP\n")

    # Write per-class header files
    header_filenames = []
    for class_name in sorted(class_funcs.keys()):
        header_name = f"{class_name.lower()}.hpp"
        header_filenames.append(header_name)
        header_path = os.path.join(cpp_dir, header_name)
        guard = f"VAKYUME_{class_name.upper()}_HPP"
        with open(header_path, "w") as hf:
            hf.write(f"#ifndef {guard}\n#define {guard}\n\n")
            hf.write(_header_preamble + "\n")
            if class_needs_lambertw.get(class_name):
                hf.write('#include "lambertw.hpp"\n\n')
            for func in class_funcs[class_name]:
                hf.write(func + "\n\n")
            hf.write(f"#endif // {guard}\n")
        print(f"  Header: {header_path}")

    # ── Write main.cpp ───────────────────────────────────────────────────
    main_cpp_path = os.path.join(cpp_dir, "main.cpp")

    main_lines = [
        "#include <iostream>",
        "",
    ]
    for hf in sorted(header_filenames):
        main_lines.append(f'#include "{hf}"')
    main_lines.append("")

    test_suite = [
        "int main() {",
        '    std::cout << "Running test suite..." << std::endl;',
        "    int pass = 0, fail = 0;",
    ]

    for class_name, method_name, source, args in tasks_to_run:
        real_args = [a for a in args if a not in ("self", "cls")]
        dummy_args = ", ".join(["1.0"] * len(real_args))
        full_name = f"{class_name}_{method_name}"
        test_suite.append(f'    std::cout << "Testing {full_name}... ";')
        test_suite.append(
            f"    try {{ auto r = {full_name}({dummy_args}); "
            f'std::cout << "OK (" << r.size() << " results)" << std::endl; pass++; }}'
            f' catch(std::exception& e) {{ std::cout << "FAIL: " << e.what() << std::endl; fail++; }}'
            f' catch(...) {{ std::cout << "FAIL" << std::endl; fail++; }}'
        )

    test_suite.append("")
    test_suite.append(
        '    std::cout << "\\n" << pass << " passed, " << fail << " failed." << std::endl;'
    )
    test_suite.append("    return fail > 0 ? 1 : 0;")
    test_suite.append("}")

    main_lines.extend(test_suite)

    with open(main_cpp_path, "w") as f:
        f.write("\n".join(main_lines))

    print(f"C++ main: {main_cpp_path}")
    print("Compiling...")
    compile_res = subprocess.run(
        [
            "g++",
            "-std=c++17",
            main_cpp_path,
            "-o",
            test_binary_path,
            "-lm",
            "-I",
            cpp_dir,
        ],
        capture_output=True,
        text=True,
    )
    if compile_res.returncode != 0:
        print("Compilation FAILED:")
        print(compile_res.stderr)
    else:
        print(f"Compilation SUCCESS. Binary: {test_binary_path}")
        print("Running tests...")
        run_res = subprocess.run([test_binary_path], capture_output=True, text=True)
        print(run_res.stdout)
        if run_res.returncode != 0:
            print(f"Tests exited with code {run_res.returncode}")


if __name__ == "__main__":
    main()
