"""Derivation-scaffold generation for LLM-assisted equation solving.

When SymPy cannot isolate a variable algebraically, this module builds a
partially-completed code scaffold that guides the LLM (Phi-3) to fill in the
final expression.  Each pattern handler recognises a specific algebraic form
and returns a scaffold string, or ``None`` to fall through to the next handler.
"""

import re

# ---------------------------------------------------------------------------
#  Shared helper utilities
# ---------------------------------------------------------------------------


def _extract_outer_coeff(expr: str, pow_fragment: str) -> str:
    """Given ``'A * B * (fragment)**exp'``, return ``'A * B'``.

    Everything *except* *pow_fragment* itself.  Returns ``""`` when the
    outer coefficient is trivially ``1``.
    """
    rest = expr.replace(pow_fragment, "1").strip()
    rest = re.sub(r"\b1\s*\*\s*", "", rest)
    rest = re.sub(r"\s*\*\s*1\b", "", rest)
    rest = rest.strip()
    return rest if rest and rest != "1" else ""


def _remove_target_diffs(expr: str, target_var: str) -> str:
    """Remove ``(target - X)`` factors from *expr*, leaving other factors."""
    cleaned = re.sub(
        r"\(\s*" + re.escape(target_var) + r"\s*-\s*\w+\s*\)\s*\*?\s*",
        "",
        expr,
    )
    cleaned = cleaned.strip().strip("*").strip()
    return cleaned if cleaned else "1"


def _strip_balanced_outer_parens(text: str) -> str:
    """Strip one layer of balanced outer parentheses if present."""
    text = text.strip()
    if text.startswith("(") and text.endswith(")"):
        depth = 0
        for i, c in enumerate(text):
            if c == "(":
                depth += 1
            elif c == ")":
                depth -= 1
            if depth == 0 and i < len(text) - 1:
                return text  # parens close before the end — not wrapping
        return text[1:-1]
    return text


def _extract_inner_expr(eq: str, exp_val: str) -> str:
    """Walk backwards from ``**<exp_val>`` to find the parenthesized expression
    that is raised to the power, returning its inner content (without the
    outer parens).
    """
    exp_pattern = re.escape("**" + exp_val)
    exp_pos_match = re.search(r"\*\*\s*" + re.escape(exp_val), eq)
    if not exp_pos_match:
        return ""
    exp_pos = exp_pos_match.start()

    start = exp_pos - 1
    while start >= 0 and eq[start] == " ":
        start -= 1
    if start < 0 or eq[start] != ")":
        return ""
    end_p = start
    depth = 1
    start -= 1
    while start >= 0 and depth > 0:
        if eq[start] == ")":
            depth += 1
        elif eq[start] == "(":
            depth -= 1
        start -= 1
    # Check if there's a matching ( before start
    return eq[start + 2 : end_p]


def _split_num_den(inner_expr: str) -> tuple[str, str]:
    """Split *inner_expr* into (numerator, denominator) around ``' / '``.

    Balanced outer parens on the denominator are stripped.
    """
    if " / " not in inner_expr:
        return inner_expr, ""
    num_part, den_part = inner_expr.split(" / ", 1)
    den_part = _strip_balanced_outer_parens(den_part.strip())
    return num_part.strip(), den_part


def _resolve_outer_mult(
    rhs: str,
    inner_expr: str,
    exp_val: str,
    lhs: str,
    inv_exp: str,
) -> tuple[str, str]:
    """Return ``(outer_mult, R_expr)`` where *R_expr* is the expression for the
    ratio with the exponent cleared.
    """
    outer_mult = _extract_outer_coeff(rhs, f"({inner_expr})**{exp_val}")
    if not outer_mult:
        outer_mult = _extract_outer_coeff(rhs, f"({inner_expr}) ** {exp_val}")
    if not outer_mult:
        outer_mult = _extract_outer_coeff(rhs, f"({inner_expr})")
    if outer_mult:
        R_expr = f"({lhs} / ({outer_mult})) ** ({inv_exp})"
    else:
        R_expr = f"({lhs}) ** ({inv_exp})"
    return outer_mult, R_expr


def _clean_remaining(expr: str, fragment: str) -> str:
    """Replace *fragment* with ``1`` in *expr* then tidy up stray ``* 1``."""
    r = expr.replace(fragment, "1")
    r = re.sub(r"\b1\s*\*\s*", "", r)
    r = re.sub(r"\s*\*\s*1\b", "", r)
    r = r.strip().strip("*").strip()
    return r if r and r != "1" else ""


def _build_brentq_scaffold(
    header: str,
    pyeqn: str,
    target_var: str,
    lhs: str,
    rhs: str,
    total: int,
    comment: str,
    start_expr: str = "0.001",
) -> str:
    """Build a complete ``brentq`` numerical-solver scaffold with robust range search."""
    rhs_sub = re.sub(
        r"\b" + re.escape(target_var) + r"\b",
        target_var + "_val",
        rhs,
    )
    lines = [header]
    lines.append(f"    # [.pyeqn] {pyeqn}")
    lines.append(f"    # {comment}")
    lines.append(f"    from scipy.optimize import brentq")
    lines.append(f"    import numpy as np")
    lines.append(f"    def _res({target_var}_val):")
    lines.append(f"        try:")
    lines.append(
        f"            # Force complex evaluation to handle negative bases in fractional powers"
    )
    lines.append(f"            target_var_complex = complex({target_var}_val, 0)")
    rhs_complex = rhs_sub.replace(target_var + "_val", "target_var_complex")
    lines.append(f"            val = {rhs_complex} - {lhs}")
    lines.append(f"            return val.real if hasattr(val, 'real') else val")
    lines.append(f"        except Exception:")
    lines.append(f"            return float('nan')")
    lines.append(f"    lo, hi = None, None")
    lines.append(
        f"    # Expanded search: log-space from 1e-6 to 1e6 plus some linear steps"
    )
    lines.append(f"    search_points = np.logspace(-6, 6, 500)")
    lines.append(f"    for i in range(len(search_points)-1):")
    lines.append(f"        p1, p2 = search_points[i], search_points[i+1]")
    lines.append(f"        r1, r2 = _res(p1), _res(p2)")
    lines.append(f"        if np.isfinite(r1) and np.isfinite(r2) and r1 * r2 <= 0:")
    lines.append(f"            lo, hi = p1, p2")
    lines.append(f"            break")
    lines.append(f"    if lo is None:")
    lines.append(f"        # Fallback to a wider linear search if logspace fails")
    lines.append(f"        for x in np.linspace(0.001, 10000, 1000):")
    lines.append(f"            r = _res(x)")
    lines.append(f"            if np.isfinite(r):")
    lines.append(f"                if lo is None: lo_val, lo = r, x")
    lines.append(f"                if r * lo_val <= 0:")
    lines.append(f"                    hi = x")
    lines.append(f"                    break")
    lines.append(f"    if lo is None or hi is None:")
    lines.append(
        f'        raise UnsolvedException("No sign change found for {target_var} in expanded range")'
    )
    lines.append(f"    {target_var} = brentq(_res, lo, hi)")
    lines.append(f"    return [{target_var}]")
    return "\n".join(lines)


