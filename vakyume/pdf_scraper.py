def scrape_pdf(
    pdf_path: str,
    output_dir: str,
    skip_chapters: set[str] | None = None,
    verbose: bool = True,
    chapter_filter: list[int] | None = None,
    model: str = LLM_MODEL,
) -> dict:
    """Extract equations from a PDF textbook and write vakyume notes files.

    Args:
        pdf_path:       Path to the PDF file.
        output_dir:     Directory to write notes files into.
        skip_chapters:  Chapter titles to skip (no extractable equations).
        verbose:        Print progress info.
        chapter_filter: If set, only process these chapter numbers (1-indexed).
        model:          Ollama model name to use for extraction (default: llama3:latest).

    Returns:
        Summary dict with per-chapter equation counts.
    """
    if skip_chapters is None:
        skip_chapters = SKIP_CHAPTERS

    os.makedirs(output_dir, exist_ok=True)

    chapters = _extract_chapter_map(pdf_path)
    summary = {}

    for ch in chapters:
        ch_num = ch["chapter_num"]
        ch_title = ch["title"]

        if ch_title in skip_chapters:
            if verbose:
                print(f"[SKIP] Chapter {ch_num}: {ch_title} (in skip list)")
            continue

        if chapter_filter and ch_num not in chapter_filter:
            if verbose:
                print(f"[SKIP] Chapter {ch_num}: {ch_title} (not in filter)")
            continue

        if verbose:
            print(f"\n{'=' * 60}")
            print(f"[SCRAPING] Chapter {ch_num}: {ch_title}")
            print(f"  Pages {ch['start_page'] + 1} - {ch['end_page'] + 1}")
            print(f"{'=' * 60}")

        # Extract and chunk the text
        raw_text = _extract_pages_text(pdf_path, ch["start_page"], ch["end_page"])
        chunks = _chunk_text(raw_text)

        if verbose:
            print(f"  Split into {len(chunks)} chunks")

        all_equations = []

        for i, chunk in enumerate(chunks):
            if verbose:
                print(
                    f"  [Chunk {i + 1}/{len(chunks)}] Sending to LLM ({len(chunk)} chars)..."
                )

            try:
                raw_response = _ask_llm_extract(chunk, ch_num, ch_title, model=model)
                parsed = _parse_llm_output(raw_response)

                if verbose:
                    if parsed:
                        print(f"    -> Extracted {len(parsed)} equation(s)")
                        for eq in parsed:
                            print(f"       {eq['header']}")
                    else:
                        print(f"    -> No equations in this chunk")

                all_equations.extend(parsed)

            except Exception as e:
                print(f"  [ERROR] Chunk {i + 1}: {e}")
                continue

        # Deduplicate
        all_equations = _dedup_equations(all_equations)

        if all_equations:
            # Write notes file
            fname = f"{ch_num:02d}_{_slugify(ch_title)}.py"
            fpath = os.path.join(output_dir, fname)
            content = _format_notes_file(ch_num, ch_title, all_equations)
            with open(fpath, "w") as f:
                f.write(content)

            if verbose:
                print(f"\n  [SAVED] {fpath} ({len(all_equations)} equations)")

            summary[ch_title] = {
                "file": fname,
                "equations": len(all_equations),
                "eq_ids": [eq["header"] for eq in all_equations],
            }
        else:
            if verbose:
                print(f"\n  [EMPTY] No equations found in Chapter {ch_num}")
            summary[ch_title] = {"file": None, "equations": 0, "eq_ids": []}

    # Write summary JSON
    summary_path = os.path.join(output_dir, "_extraction_summary.json")
    with open(summary_path, "w") as f:
        json.dump(summary, f, indent=2)

    total = sum(v["equations"] for v in summary.values())
    if verbose:
        print(f"\n{'=' * 60}")
        print(f"EXTRACTION COMPLETE")
        print(f"  Total equations extracted: {total}")
        print(
            f"  Chapters processed: {len([v for v in summary.values() if v['equations'] > 0])}"
        )
        print(f"  Summary: {summary_path}")
        print(f"{'=' * 60}")

    return summary


def _slugify(title: str) -> str:
    """Convert a chapter title to a filename-safe slug."""
    slug = title.lower()
    slug = re.sub(r"[^a-z0-9]+", "_", slug)
    slug = slug.strip("_")
    return slug[:50]


# тФАтФАтФА Interactive wizard тФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФА


def _detect_ollama_models() -> list[str]:
    """Query ollama for available models. Returns a list of model names."""
    try:
        import ollama as _ollama

        models = _ollama.list()
        # ollama.list() returns a dict with "models" key or a ListResponse
        if hasattr(models, "models"):
            return [m.model for m in models.models]
        elif isinstance(models, dict) and "models" in models:
            return [m["name"] for m in models["models"]]
        return []
    except Exception:
        return []


def _wizard_select_model() -> str:
    """Interactive model selection. Returns the chosen model name."""
    print("\n--- Model Selection ---")
    available = _detect_ollama_models()

    if not available:
        print("  Could not detect Ollama models. Using default: llama3:latest")
        return "llama3:latest"

    # Prefer llama3 as default, but show all options
    default_idx = 0
    for i, name in enumerate(available):
        if "llama3" in name and "llama3.2" not in name:
            default_idx = i
            break

    print("  Available Ollama models:")
    for i, name in enumerate(available):
        marker = " (default)" if i == default_idx else ""
        print(f"    [{i + 1}] {name}{marker}")

    choice = input(
        f"\n  Select model [1-{len(available)}] (Enter for default): "
    ).strip()

    if not choice:
        selected = available[default_idx]
    else:
        try:
            idx = int(choice) - 1
            if 0 <= idx < len(available):
                selected = available[idx]
            else:
                print(f"  Invalid selection. Using default: {available[default_idx]}")
                selected = available[default_idx]
        except ValueError:
            # Allow typing a model name directly
            if choice in available:
                selected = choice
            else:
                print(f"  Unknown model. Using default: {available[default_idx]}")
                selected = available[default_idx]

    print(f"  -> Using model: {selected}")
    return selected


def _wizard_select_chapters(pdf_path: str) -> list[int] | None:
    """Interactive chapter selection. Returns list of chapter numbers or None for all."""
    chapters = _extract_chapter_map(pdf_path)
    n = len(chapters)

    print("\n--- Chapter Selection ---")
    print(f"  Found {n} chapters in PDF:\n")

    for ch in chapters:
        skip = (
            " [auto-skip: non-equation content]" if ch["title"] in SKIP_CHAPTERS else ""
        )
        pages = ch["end_page"] - ch["start_page"] + 1
        print(f"    [{ch['chapter_num']:2d}] {ch['title']} ({pages} pages){skip}")

    print(f"\n  Options:")
    print(f"    - Enter chapter numbers separated by commas (e.g., 3,5,9)")
    print(f"    - Enter a range with a dash (e.g., 3-9)")
    print(f"    - Press Enter to process ALL chapters")
    print(f"    - Type 'one' to pick a single chapter (good for testing)")

    choice = input("\n  Chapters to process: ").strip()

    if not choice:
        print("  -> Processing ALL chapters")
        return None

    if choice.lower() == "one":
        ch_input = input("  Enter chapter number: ").strip()
        try:
            ch_num = int(ch_input)
            if 1 <= ch_num <= n:
                print(f"  -> Selected chapter {ch_num}")
                return [ch_num]
            else:
                print(f"  Invalid chapter number. Processing ALL chapters.")
                return None
        except ValueError:
            print(f"  Invalid input. Processing ALL chapters.")
            return None

    # Parse comma-separated and ranges
    selected = []
    for part in choice.split(","):
        part = part.strip()
        if "-" in part:
            try:
                start, end = part.split("-", 1)
                selected.extend(range(int(start.strip()), int(end.strip()) + 1))
            except ValueError:
                print(f"  Warning: Could not parse range '{part}', skipping")
        else:
            try:
                selected.append(int(part))
            except ValueError:
                print(f"  Warning: Could not parse '{part}', skipping")

    valid = [n for n in selected if 1 <= n <= len(chapters)]
    if not valid:
        print("  No valid chapters selected. Processing ALL chapters.")
        return None

    print(f"  -> Selected {len(valid)} chapter(s): {valid}")
    return valid


def _wizard_confirm(
    pdf_path: str, output_dir: str, model: str, chapters: list[int] | None
) -> bool:
    """Show summary and ask for confirmation before starting."""
    print(f"\n--- Extraction Plan ---")
    print(f"  PDF:        {pdf_path}")
    print(f"  Output:     {output_dir}")
    print(f"  Model:      {model}")
    if chapters:
        print(f"  Chapters:   {chapters}")
    else:
        print(f"  Chapters:   ALL (excluding non-equation chapters)")

    # Rough time estimate: ~5s per chunk * ~10 chunks per chapter
    ch_count = len(chapters) if chapters else 20  # rough estimate for "all"
    est_minutes = (ch_count * 10 * 5) / 60
    print(f"  Est. time:  ~{est_minutes:.0f} minutes (depends on model speed)")

    confirm = input("\n  Proceed? [Y/n] ").strip().lower()
    return confirm in ("", "y", "yes")


def run_wizard(
    pdf_path: str,
    output_dir: str | None = None,
    model_override: str | None = None,
) -> dict | None:
    """Interactive wizard for PDF equation extraction.

    Walks the user through model selection, chapter selection, and confirmation
    before running the extraction.

    Args:
        pdf_path:       Path to the PDF file.
        output_dir:     Output directory (default: notes/ next to PDF).
        model_override: If set, skip model selection and use this model.

    Returns:
        Extraction summary dict, or None if the user cancelled.
    """
    print("=" * 60)
    print("  VAKYUME PDF Equation Scraper - Interactive Wizard")
    print("=" * 60)
    print(f"\n  PDF: {pdf_path}")

    if output_dir is None:
        output_dir = os.path.join(os.path.dirname(pdf_path), "notes")

    # Step 1: Model selection (skip if caller already chose)
    if model_override:
        model = model_override
        print(f"\n  Model: {model} (from --model flag)")
    else:
        model = _wizard_select_model()

    # Step 2: Chapter selection
    chapters = _wizard_select_chapters(pdf_path)

    # Step 3: Confirm
    if not _wizard_confirm(pdf_path, output_dir, model, chapters):
        print("\n  Cancelled.")
        return None

    print(f"\n{'=' * 60}")
    print(f"  Starting extraction...")
    print(f"{'=' * 60}\n")

    return scrape_pdf(
        pdf_path=pdf_path,
        output_dir=output_dir,
        verbose=True,
        chapter_filter=chapters,
        model=model,
    )


