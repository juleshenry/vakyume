def _extract_chapter_map(pdf_path: str) -> list[dict]:
    """Return a list of chapters with page ranges extracted from the PDF TOC.

    Each entry: {"title": str, "start_page": int, "end_page": int, "chapter_num": int}
    Pages are 0-indexed (PyMuPDF convention).
    """
    doc = fitz.open(pdf_path)
    toc = doc.get_toc()
    total_pages = len(doc)
    doc.close()

    # Gather L1 (chapter-level) entries
    l1_entries = [
        (entry[1], entry[2] - 1) for entry in toc if entry[0] == 1
    ]  # 0-indexed

    chapters = []
    for i, (title, start_page) in enumerate(l1_entries):
        end_page = (
            l1_entries[i + 1][1] - 1 if i + 1 < len(l1_entries) else total_pages - 1
        )
        chapters.append(
            {
                "title": title,
                "start_page": start_page,
                "end_page": end_page,
                "chapter_num": i + 1,
            }
        )
    return chapters


def _extract_pages_text(pdf_path: str, start_page: int, end_page: int) -> str:
    """Extract raw text from a range of PDF pages."""
    doc = fitz.open(pdf_path)
    texts = []
    for pg_num in range(start_page, min(end_page + 1, len(doc))):
        page = doc[pg_num]
        texts.append(page.get_text())
    doc.close()
    return "\n".join(texts)


def _chunk_text(text: str, max_chars: int = CHUNK_MAX_CHARS) -> list[str]:
    """Split text into chunks that fit within the LLM context window.

    Splits on paragraph boundaries (double newlines) to avoid cutting
    mid-equation.
    """
    paragraphs = re.split(r"\n\n+", text)
    chunks = []
    current = ""
    for para in paragraphs:
        if len(current) + len(para) + 2 > max_chars and current:
            chunks.append(current.strip())
            current = para
        else:
            current = current + "\n\n" + para if current else para
    if current.strip():
        chunks.append(current.strip())
    return chunks


