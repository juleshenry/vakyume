def main():
    parser = argparse.ArgumentParser(
        description="Extract equations from a PDF textbook into vakyume notes format."
    )
    parser.add_argument("pdf", help="Path to the PDF file")
    parser.add_argument(
        "--output-dir",
        "-o",
        default=None,
        help="Output directory for notes files (default: notes/ next to PDF)",
    )
    parser.add_argument(
        "--chapters",
        "-c",
        type=int,
        nargs="+",
        help="Only process specific chapter numbers (1-indexed)",
    )
    parser.add_argument(
        "--model",
        "-m",
        default=LLM_MODEL,
        help=f"Ollama model to use (default: {LLM_MODEL})",
    )
    parser.add_argument(
        "--list-chapters",
        action="store_true",
        help="List available chapters and exit",
    )
    parser.add_argument(
        "--wizard",
        "-w",
        action="store_true",
        help="Run the interactive wizard (guided chapter and model selection)",
    )
    parser.add_argument(
        "--quiet", "-q", action="store_true", help="Suppress progress output"
    )
    args = parser.parse_args()

    if args.list_chapters:
        chapters = _extract_chapter_map(args.pdf)
        for ch in chapters:
            skip = " [SKIP]" if ch["title"] in SKIP_CHAPTERS else ""
            print(
                f"  {ch['chapter_num']:2d}. {ch['title']} (p.{ch['start_page'] + 1}){skip}"
            )
        return

    output_dir = args.output_dir
    if output_dir is None:
        output_dir = os.path.join(os.path.dirname(args.pdf), "notes")

    if args.wizard:
        run_wizard(
            pdf_path=args.pdf,
            output_dir=output_dir,
            model_override=args.model if args.model != LLM_MODEL else None,
        )
    else:
        scrape_pdf(
            pdf_path=args.pdf,
            output_dir=output_dir,
            verbose=not args.quiet,
            chapter_filter=args.chapters,
            model=args.model,
        )


if __name__ == "__main__":
    main()
