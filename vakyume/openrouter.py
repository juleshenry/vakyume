"""OpenRouter-compatible LLM provider.

Routes requests through the OpenRouter API (https://openrouter.ai), which
gives unified access to OpenAI, Anthropic, Google Gemini, and many other
models using a single API key.  Also supports direct OpenAI / Anthropic /
Gemini keys when the user supplies a ``provider`` + ``api_key`` pair instead.

The public entry point is :func:`openrouter_chat`.  It speaks the
OpenAI-compatible ``/v1/chat/completions`` protocol that OpenRouter (and
the official OpenAI SDK) understand, so a single code path covers all
providers.
"""

import json
import os
from typing import Optional

import httpx

# ---------------------------------------------------------------------------
#  Provider base URLs
# ---------------------------------------------------------------------------
_PROVIDER_URLS = {
    "openrouter": "https://openrouter.ai/api/v1/chat/completions",
    "openai": "https://api.openai.com/v1/chat/completions",
    "anthropic": "https://api.anthropic.com/v1/messages",
    "gemini": "https://generativelanguage.googleapis.com/v1beta/openai/chat/completions",
}

# Default models per provider (sensible, cost-effective picks)
_DEFAULT_MODELS = {
    "openrouter": "openai/gpt-4o-mini",
    "openai": "gpt-4o-mini",
    "anthropic": "claude-sonnet-4-20250514",
    "gemini": "gemini-2.0-flash",
}


# ---------------------------------------------------------------------------
#  Anthropic adapter
# ---------------------------------------------------------------------------
def _anthropic_chat(
    api_key: str,
    model: str,
    messages: list[dict],
    temperature: float = 0,
    max_tokens: int = 4096,
) -> str:
    """Call the Anthropic Messages API and return the assistant text."""
    url = _PROVIDER_URLS["anthropic"]

    # Anthropic expects system prompt separated from messages
    system_text = ""
    user_messages = []
    for msg in messages:
        if msg["role"] == "system":
            system_text += msg["content"] + "\n"
        else:
            user_messages.append(msg)

    payload = {
        "model": model,
        "max_tokens": max_tokens,
        "temperature": temperature,
        "messages": user_messages,
    }
    if system_text.strip():
        payload["system"] = system_text.strip()

    headers = {
        "x-api-key": api_key,
        "anthropic-version": "2023-06-01",
        "content-type": "application/json",
    }

    with httpx.Client(timeout=120) as client:
        resp = client.post(url, headers=headers, json=payload)
        resp.raise_for_status()
        data = resp.json()

    # Anthropic returns content as a list of blocks
    blocks = data.get("content", [])
    text_parts = [b["text"] for b in blocks if b.get("type") == "text"]
    return "".join(text_parts)


# ---------------------------------------------------------------------------
#  OpenAI-compatible chat (covers OpenRouter, OpenAI, Gemini)
# ---------------------------------------------------------------------------
def _openai_compatible_chat(
    url: str,
    api_key: str,
    model: str,
    messages: list[dict],
    temperature: float = 0,
    max_tokens: int = 4096,
    extra_headers: Optional[dict] = None,
) -> str:
    """POST to any OpenAI-compatible chat/completions endpoint."""
    headers = {
        "Authorization": f"Bearer {api_key}",
        "Content-Type": "application/json",
    }
    if extra_headers:
        headers.update(extra_headers)

    payload = {
        "model": model,
        "messages": messages,
        "temperature": temperature,
        "max_tokens": max_tokens,
    }

    with httpx.Client(timeout=120) as client:
        resp = client.post(url, headers=headers, json=payload)
        resp.raise_for_status()
        data = resp.json()

    return data["choices"][0]["message"]["content"]


# ---------------------------------------------------------------------------
#  Unified entry point
# ---------------------------------------------------------------------------
def openrouter_chat(
    system_prompt: str,
    user_prompt: str,
    provider: str = "openrouter",
    model: Optional[str] = None,
    api_key: Optional[str] = None,
    temperature: float = 0,
    max_tokens: int = 4096,
) -> str:
    """Send a system+user message pair to the chosen provider.

    Parameters
    ----------
    provider : str
        One of ``"openrouter"``, ``"openai"``, ``"anthropic"``, ``"gemini"``.
    model : str or None
        Model identifier.  If *None*, a sensible default is chosen per provider.
    api_key : str or None
        API key.  Falls back to the environment variables
        ``OPENROUTER_API_KEY``, ``OPENAI_API_KEY``, ``ANTHROPIC_API_KEY``,
        or ``GEMINI_API_KEY`` respectively.
    """
    provider = provider.lower()

    # Resolve API key from env if not given
    if api_key is None:
        env_map = {
            "openrouter": "OPENROUTER_API_KEY",
            "openai": "OPENAI_API_KEY",
            "anthropic": "ANTHROPIC_API_KEY",
            "gemini": "GEMINI_API_KEY",
        }
        env_var = env_map.get(provider)
        if env_var:
            api_key = os.environ.get(env_var, "")
        if not api_key:
            raise RuntimeError(
                f"No API key for provider '{provider}'.  "
                f"Set the {env_var} environment variable or pass --llm-api-key."
            )

    model = model or _DEFAULT_MODELS.get(provider, "gpt-4o-mini")

    messages = [
        {"role": "system", "content": system_prompt},
        {"role": "user", "content": user_prompt},
    ]

    if provider == "anthropic":
        return _anthropic_chat(
            api_key=api_key,
            model=model,
            messages=messages,
            temperature=temperature,
            max_tokens=max_tokens,
        )

    # OpenRouter, OpenAI, Gemini all use OpenAI-compatible protocol
    url = _PROVIDER_URLS.get(provider)
    if not url:
        raise ValueError(
            f"Unknown provider '{provider}'.  "
            f"Choose from: {', '.join(_PROVIDER_URLS.keys())}"
        )

    extra_headers = {}
    if provider == "openrouter":
        extra_headers["HTTP-Referer"] = "https://github.com/vakyume"
        extra_headers["X-Title"] = "vakyume"

    return _openai_compatible_chat(
        url=url,
        api_key=api_key,
        model=model,
        messages=messages,
        temperature=temperature,
        max_tokens=max_tokens,
        extra_headers=extra_headers,
    )
