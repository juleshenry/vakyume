class Verify:
    def __init__(self, lib_class, pyeqn=None, harmony_checks_dir=None, verbose=False):
        self.lib_class = lib_class
        self.verbose = verbose
        self._log_buffer = []
        # Instantiate the class since methods are now instance methods
        try:
            self.lib_instance = lib_class()
        except Exception as e:
            self._log_buffer.append(
                f"  [Verify] WARNING: Could not instantiate {lib_class.__name__}: {e}. Falling back to class-level calls."
            )
            self.lib_instance = lib_class

        self.pyeqn = pyeqn
        self.harmony_checks_dir = harmony_checks_dir
        self.harmony_func = None
        self.base_equations = [
            name
            for name in dir(lib_class)
            if name.startswith("eqn") and "__" not in name
        ]
        self.equation_variants = [
            name for name in dir(lib_class) if name.startswith("eqn") and "__" in name
        ]

        if self.harmony_checks_dir and self.pyeqn:
            self._prepare_harmony_subshard()

    def _prepare_harmony_subshard(self):
        try:
            lhs_str, rhs_str = self.pyeqn.split("=", 1)
            lhs_str = lhs_str.split("#")[0].strip()
            rhs_str = rhs_str.split("#")[0].strip()

            import hashlib

            h = hashlib.md5(self.pyeqn.encode()).hexdigest()
            module_name = f"harmony_check_{h}"
            subshard_path = os.path.join(self.harmony_checks_dir, f"{module_name}.py")

            if not os.path.exists(subshard_path):
                # Collect known equation variables from variant names
                # e.g. eqn_8_9__e, eqn_8_9__r → {'e', 'r', ...}
                known_vars = set()
                for v in self.equation_variants:
                    if "__" in v:
                        known_vars.add(v.split("__")[-1])
                # Also collect from function signatures
                for v in self.equation_variants:
                    try:
                        sig = inspect.signature(getattr(self.lib_class, v))
                        for name, param in sig.parameters.items():
                            if (
                                param.kind
                                not in (param.VAR_POSITIONAL, param.VAR_KEYWORD)
                                and name != "self"
                            ):
                                known_vars.add(name)
                    except Exception:
                        pass

                # Find all identifiers in lhs and rhs to use as arguments
                all_tokens = set(
                    re.findall(r"\b[a-zA-Z_]\w*\b", lhs_str + " " + rhs_str)
                )
                # Remove math functions and numbers, BUT keep tokens that
                # are known equation variables (e.g. 'e' in eqn_8_9)
                math_funcs = {
                    "log",
                    "sqrt",
                    "exp",
                    "pow",
                    "e",
                    "pi",
                    "sin",
                    "cos",
                    "tan",
                    "ln",
                }
                tokens = sorted(
                    [
                        t
                        for t in all_tokens
                        if (t in known_vars or t not in math_funcs) and not t.isdigit()
                    ]
                )

                with open(subshard_path, "w") as f:
                    f.write(
                        "from cmath import *\nfrom math import e, pi\nimport numpy as np\n\n"
                    )
                    f.write(f"def check_harmony({', '.join(tokens)}, **kwargs):\n")
                    f.write(
                        f"    res = ({lhs_str.replace('ln', 'log')}) - ({rhs_str.replace('ln', 'log')})\n"
                    )
                    f.write(
                        f"    # Ensure we return a magnitude (abs) to avoid truth-value ambiguity for complex residuals\n"
                    )
                    f.write(f"    return abs(res)\n")

            if self.harmony_checks_dir not in sys.path:
                sys.path.append(self.harmony_checks_dir)

            importlib.invalidate_caches()
            if module_name in sys.modules:
                module = importlib.reload(sys.modules[module_name])
            else:
                module = importlib.import_module(module_name)

            self.harmony_func = module.check_harmony
        except Exception as err:
            self._log_buffer.append(f"  [Harmony Prep] ERROR: {err}")

    def _get_params(self, base_eq):
        variants = [v for v in self.equation_variants if v.startswith(base_eq + "__")]
        params = set()
        for v in variants:
            sig = inspect.signature(getattr(self.lib_class, v))
            for name, param in sig.parameters.items():
                if (
                    param.kind not in (param.VAR_POSITIONAL, param.VAR_KEYWORD)
                    and name != "self"
                ):
                    params.add(name)
            params.add(v.split("__")[-1])
        return sorted(list(params))

    def _check_pyeqn_harmony(self, pyeqn, params_dict, log_lines=None):
        """Checks if the values satisfy the original equation string."""
        if not pyeqn or "=" not in pyeqn:
            return True

        if self.harmony_func:
            try:
                res_val = self.harmony_func(**params_dict)
                if isinstance(res_val, (list, tuple, np.ndarray)):
                    res_val = res_val[0]

                # Handle SymPy expressions if they leak through
                if hasattr(res_val, "evalf"):
                    try:
                        res_val = complex(res_val.evalf())
                    except (TypeError, ValueError):
                        pass

                res = abs(res_val) < 1e-4
                if not res:
                    msg = f"  [Harmony Check] FAIL: residual {res_val} for {pyeqn}"
                    if log_lines is not None:
                        log_lines.append(msg)
                    else:
                        print(msg)
                return res
            except Exception as err:
                # If the harmony subshard crashes for any reason, treat as
                # inconclusive rather than a failure.
                msg = f"  [Harmony Check] ERROR during subshard call: {err}"
                if log_lines is not None:
                    log_lines.append(msg)
                else:
                    print(msg)
                return None

        # No fallback to eval anymore as per user request
        return True

    @staticmethod
    def make_rand():
        # Avoid 0 and 1 to prevent division by zero or log(1) which might be trivial
        # Also avoid negative numbers for logs/roots
        return round(random.uniform(1.1, 10.0), 5)

    def are_similar(self, a, b):
        if a is None or b is None:
            return False

        def to_scalar(v):
            """Convert a single (non-list) value to float or complex.
            Always returns a complex number (or None) — the caller
            compares real and imaginary parts independently."""
            if v is None:
                return None
            if isinstance(v, (int, float)):
                if not np.isfinite(v):
                    return None
                return complex(float(v), 0)
            if isinstance(v, complex):
                if not (np.isfinite(v.real) and np.isfinite(v.imag)):
                    return None
                return v
            try:
                if hasattr(v, "evalf"):
                    ev = v.evalf()
                    try:
                        f_ev = float(ev)
                        if not np.isfinite(f_ev):
                            return None
                        return complex(f_ev, 0)
                    except (TypeError, ValueError):
                        c_ev = complex(ev)
                        if not (np.isfinite(c_ev.real) and np.isfinite(c_ev.imag)):
                            return None
                        return c_ev
                f_v = float(v)
                if not np.isfinite(f_v):
                    return None
                return complex(f_v, 0)
            except (TypeError, ValueError):
                try:
                    c_v = complex(v)
                    if not (np.isfinite(c_v.real) and np.isfinite(c_v.imag)):
                        return None
                    return c_v
                except (TypeError, ValueError):
                    return None

        def scalars_match(sa, sb):
            if sa is None or sb is None:
                return False
            # Both are complex — check real and imaginary parts independently
            # Strip negligible imaginary parts (numerical noise from
            # fractional-power computations can produce ~1e-6j residuals)
            if abs(sa.imag) < 1e-4:
                sa = complex(sa.real, 0)
            if abs(sb.imag) < 1e-4:
                sb = complex(sb.real, 0)

            # Use both absolute and relative tolerance for robustness
            abs_tol = 1e-4
            rel_tol = 1e-6

            def _close(x, y):
                diff = abs(x - y)
                mag = max(abs(x), abs(y), 1e-15)
                return diff < abs_tol or diff / mag < rel_tol

            return _close(sa.real, sb.real) and _close(sa.imag, sb.imag)

        # If either side is a list, check whether ANY element from one side
        # matches ANY element (or the scalar) on the other side.
        a_is_list = isinstance(a, (list, tuple, np.ndarray))
        b_is_list = isinstance(b, (list, tuple, np.ndarray))

        if a_is_list or b_is_list:
            a_items = list(a) if a_is_list else [a]
            b_items = list(b) if b_is_list else [b]
            for av in a_items:
                for bv in b_items:
                    sa = to_scalar(av)
                    sb = to_scalar(bv)
                    if scalars_match(sa, sb):
                        return True
            return False

        # Both are scalars
        sa = to_scalar(a)
        sb = to_scalar(b)
        return scalars_match(sa, sb)

    def _detect_invariant_variants(self, base_eq, variants, params, log_lines):
        """Detect variants whose solver returns a constant regardless of inputs.

        These correspond to variables that cancel out of the equation (e.g.
        p_i / p_nc = p_i / (p - P_c)  where p_i cancels).  SymPy solves
        them as the trivial root (often 0) because the variable factors out.
        Including them in cross-verification poisons the scores of the
        *real* variants, so we exclude them.
        """
        invariant = set()
        probe_count = 3
        for var in variants:
            method = getattr(self.lib_instance, f"{base_eq}__{var}")
            probe_results = []
            for _ in range(probe_count):
                inputs = {p: self.make_rand() for p in params if p != var}
                try:
                    vals = method(**inputs)
                    # Normalise to a comparable tuple
                    if isinstance(vals, (list, tuple, np.ndarray)):
                        probe_results.append(
                            tuple(round(abs(complex(v)), 10) for v in vals)
                        )
                    elif vals is not None:
                        probe_results.append((round(abs(complex(vals)), 10),))
                    else:
                        probe_results.append(None)
                except Exception:
                    probe_results.append(None)

            # If all probes returned the exact same (non-None) value, it's invariant
            non_none = [r for r in probe_results if r is not None]
            if len(non_none) == probe_count and len(set(non_none)) == 1:
                invariant.add(var)
                log_lines.append(
                    f" |- INVARIANT detected: {var} always returns {non_none[0]} — excluding from cross-check"
                )
        return invariant

    def verify_equation(self, base_eq):
        # Collect all log output in a buffer; flush only for failures (or if verbose)
        log_lines = list(self._log_buffer)  # inherit any init/harmony-prep messages
        params = self._get_params(base_eq)
        all_variants = [p for p in params if hasattr(self.lib_class, f"{base_eq}__{p}")]

        # Detect and exclude invariant (trivially-solved) variants
        invariant_vars = self._detect_invariant_variants(
            base_eq, all_variants, params, log_lines
        )
        variants = [v for v in all_variants if v not in invariant_vars]

        results = {}  # (source_truth_var) -> matches
        mismatches = {}  # (source_truth_var) -> details

        # Give invariant variants a perfect score so they don't drag the family down
        for iv in invariant_vars:
            results[iv] = len(all_variants)

        # Increase trials to be more certain
        num_trials = 3

        for source_var in variants:
            variant_method = getattr(self.lib_instance, f"{base_eq}__{source_var}")

            trial_matches = []
            source_mismatches = []

            for trial_idx in range(num_trials):
                test_inputs = {p: self.make_rand() for p in params if p != source_var}

                try:
                    source_values = variant_method(**test_inputs.copy())
                    if not source_values:
                        trial_matches.append(0)
                        source_mismatches.append({"error": "No values returned"})
                        continue

                    best_match_for_this_trial = 0
                    trial_detail = None

                    for val in source_values:
                        # Skip non-numeric values (e.g. SymPy symbolic objects
                        # containing sympy.I that aren't plain Python numbers)
                        if not isinstance(val, (int, float, complex)):
                            continue

                        full_set = test_inputs.copy()
                        full_set[source_var] = val

                        # Harmony check with original equation string
                        if self.pyeqn:
                            harmony_res = self._check_pyeqn_harmony(
                                self.pyeqn, full_set, log_lines=log_lines
                            )
                            if harmony_res is False:
                                continue
                            elif harmony_res is None:
                                # Inconclusive (domain error), skip this value but don't count as failure
                                continue

                        matches = len(
                            invariant_vars
                        )  # invariant vars count as automatic matches
                        current_trial_mismatches = []
                        for target_var in variants:
                            if target_var == source_var:
                                matches += 1
                                continue

                            target_method = getattr(
                                self.lib_instance, f"{base_eq}__{target_var}"
                            )
                            target_inputs = {
                                p: v for p, v in full_set.items() if p != target_var
                            }

                            try:
                                target_values = target_method(**target_inputs.copy())
                                is_sim = self.are_similar(
                                    full_set[target_var], target_values
                                )
                                if is_sim:
                                    matches += 1
                                else:
                                    current_trial_mismatches.append(
                                        {
                                            "target": target_var,
                                            "expected": full_set[target_var],
                                            "got": target_values,
                                        }
                                    )
                            except Exception as err:
                                err_str = str(err).lower()
                                # "Pending LLM" and "UnsolvedException" mean
                                # the solver doesn't exist yet — inconclusive,
                                # don't penalise the source variant.
                                if any(
                                    kw in err_str
                                    for kw in (
                                        "pending llm",
                                        "unsolvedexception",
                                    )
                                ):
                                    # Treat as inconclusive — count as a match
                                    # so it doesn't drag down the source score
                                    matches += 1
                                else:
                                    current_trial_mismatches.append(
                                        {"target": target_var, "error": str(err)}
                                    )

                        if matches > best_match_for_this_trial:
                            best_match_for_this_trial = matches
                            trial_detail = {
                                "inputs": test_inputs,
                                "output": val,
                                "mismatches": current_trial_mismatches,
                            }

                    trial_matches.append(best_match_for_this_trial)
                    if trial_detail:
                        source_mismatches.append(trial_detail)

                except Exception as err:
                    err_str = str(err).lower()
                    # Numerical solvers may legitimately find no solution for
                    # random inputs (e.g. transcendental equations with no real
                    # root in the search range).  Treat these as inconclusive
                    # rather than penalising the source variant.
                    if any(
                        kw in err_str
                        for kw in (
                            "pending llm",
                            "unsolvedexception",
                            "no valid",
                        )
                    ):
                        # Don't append to trial_matches — skip this trial
                        pass
                    else:
                        trial_matches.append(0)
                        source_mismatches.append({"error": str(err)})

            if trial_matches:
                results[source_var] = max(trial_matches)
            else:
                # All trials were inconclusive (e.g. numerical solver found
                # no root for any random input set).  Give benefit of the
                # doubt — award a perfect score so the family isn't dragged
                # down by an untestable source variant.
                results[source_var] = len(all_variants)
            if results[source_var] < len(all_variants):
                # Only keep mismatches for broken variants
                mismatches[source_var] = source_mismatches

        # Decide whether to flush the log buffer
        num_v = len(all_variants)
        is_clean = num_v > 0 and all(s >= num_v for s in results.values())

        if self.verbose:
            for line in log_lines:
                print(line)
        elif not is_clean:
            # For non-verbose mode, only print the equation name and the full log on failure
            print(f"Testing {self.lib_class.__name__}::{base_eq}... FAILED")
            for line in log_lines:
                print(line)

        return {"scores": results, "mismatches": mismatches}

    # ------------------------------------------------------------------
    #  Golden-tuple round-robin verification
    # ------------------------------------------------------------------
    def verify_equation_golden(self, base_eq, num_tuples=5, pinned_tuples=None):
        """Verify shards using golden-tuple round-robin cross-validation.

        For each shard (the "oracle"), we:
          1. Feed it random inputs → get its output → golden tuple
          2. Validate the golden tuple against the harmony function
          3. Feed the golden tuple (minus each other shard's variable) to
             each other shard and check if it recovers the correct value

        If *pinned_tuples* is provided (a list of (oracle_var, full_value_dict)
        from a previous round), those tuples are reused instead of generating
        new random ones.  This ensures deterministic verification across rounds.

        Returns a dict with:
          - scores:  {var: num_passes}  (max = num_tuples * (num_shards - 1))
          - broken:  [var, ...]
          - trusted: [var, ...]
          - golden_tuples: [{inputs: ..., oracle: var, values: {...}}, ...]
          - failures: {var: [{oracle, inputs, expected, got}, ...]}
        """
        log_lines = list(self._log_buffer)
        params = self._get_params(base_eq)
        all_variants = [p for p in params if hasattr(self.lib_class, f"{base_eq}__{p}")]

        # Detect and exclude invariant variants
        invariant_vars = self._detect_invariant_variants(
            base_eq, all_variants, params, log_lines
        )
        variants = [v for v in all_variants if v not in invariant_vars]

        if not variants:
            return {
                "scores": {v: 1 for v in all_variants},
                "broken": [],
                "trusted": list(all_variants),
                "golden_tuples": [],
                "failures": {},
                "mismatches": {},
            }

        num_v = len(all_variants)

        # Reuse pinned tuples if provided, otherwise generate fresh ones
        if pinned_tuples is not None:
            golden_tuples = list(pinned_tuples)
        else:
            # Collect golden tuples from each oracle
            golden_tuples = []  # list of (oracle_var, full_value_dict)
            for oracle_var in variants:
                method = getattr(self.lib_instance, f"{base_eq}__{oracle_var}")
                for _ in range(num_tuples):
                    inputs = {p: self.make_rand() for p in params if p != oracle_var}
                    try:
                        result = method(**inputs.copy())
                        if not result:
                            continue
                        # Pick the best numeric value — prefer non-zero to avoid
                        # degenerate equation tuples (e.g., L_0=0 makes 0=0)
                        val = None
                        first_val = None
                        for v in result:
                            if isinstance(v, (int, float, complex)):
                                if first_val is None:
                                    first_val = v
                                real_part = v.real if isinstance(v, complex) else v
                                if abs(real_part) > 1e-12:
                                    val = v
                                    break
                        if val is None:
                            val = first_val  # fall back to zero if that's all we have
                        if val is None:
                            continue

                        full_set = dict(inputs)
                        full_set[oracle_var] = val

                        # Reject golden tuples where the oracle value is
                        # significantly complex — this indicates the random inputs
                        # land outside the equation's physical domain
                        if isinstance(val, complex):
                            if abs(val.imag) > 1e-6:
                                continue
                            # Negligible imaginary part — use real
                            val = val.real
                            full_set[oracle_var] = val

                        # Validate against harmony function (equation residual)
                        if self.pyeqn:
                            harmony_res = self._check_pyeqn_harmony(
                                self.pyeqn, full_set, log_lines=log_lines
                            )
                            if harmony_res is False:
                                # Oracle produced a value that doesn't satisfy the equation
                                continue
                            if harmony_res is None:
                                # Inconclusive (domain error) — skip
                                continue

                        golden_tuples.append((oracle_var, full_set))
                    except Exception:
                        # Oracle couldn't solve for these random inputs — skip
                        continue

        if not golden_tuples:
            # No oracle could produce valid golden tuples — give benefit of doubt
            log_lines.append(f" |- WARNING: No golden tuples generated for {base_eq}")
            return {
                "scores": {v: num_v for v in all_variants},
                "broken": [],
                "trusted": list(all_variants),
                "golden_tuples": [],
                "failures": {},
                "mismatches": {},
            }

        # Test each variant against all golden tuples where it's NOT the oracle
        passes = {v: 0 for v in variants}
        total_tests = {v: 0 for v in variants}
        failures = {v: [] for v in variants}

        for oracle_var, full_set in golden_tuples:
            for target_var in variants:
                if target_var == oracle_var:
                    continue
                total_tests[target_var] += 1

                target_method = getattr(self.lib_instance, f"{base_eq}__{target_var}")
                target_inputs = {p: v for p, v in full_set.items() if p != target_var}
                expected = full_set[target_var]

                try:
                    got = target_method(**target_inputs.copy())
                    if self.are_similar(expected, got):
                        passes[target_var] += 1
                    else:
                        failures[target_var].append(
                            {
                                "oracle": oracle_var,
                                "inputs": target_inputs,
                                "expected": expected,
                                "got": got,
                            }
                        )
                except Exception as err:
                    err_str = str(err).lower()
                    if any(
                        kw in err_str for kw in ("pending llm", "unsolvedexception")
                    ):
                        # Placeholder — inconclusive, don't penalize
                        passes[target_var] += 1
                    else:
                        failures[target_var].append(
                            {
                                "oracle": oracle_var,
                                "inputs": target_inputs,
                                "expected": expected,
                                "error": str(err),
                            }
                        )

        # Build scores: a shard is "perfect" if it passes ALL its tests
        # For compatibility with analyze_results, score = num_v means perfect
        scores = {}
        broken = []
        trusted = []

        for v in variants:
            if total_tests[v] == 0:
                # Never tested (e.g. only 1 variant) — benefit of doubt
                scores[v] = num_v
                trusted.append(v)
            elif passes[v] == total_tests[v]:
                scores[v] = num_v
                trusted.append(v)
            else:
                # Scale score: fraction of tests passed, mapped to 0..(num_v-1)
                frac = passes[v] / total_tests[v]
                scores[v] = max(0, int(frac * (num_v - 1)))
                broken.append(v)

        # -- Detect swapped variable pairs --
        # If two broken variants consistently return each other's expected
        # values, they are algebraically equivalent solvers that were
        # assigned to the wrong variable.  Detect and mark as swapped so
        # the repair pipeline can fix them by swapping the shard contents.
        swap_candidates = [v for v in broken]
        swapped_pairs = []
        if len(swap_candidates) >= 2:
            for i in range(len(swap_candidates)):
                for j in range(i + 1, len(swap_candidates)):
                    va, vb = swap_candidates[i], swap_candidates[j]
                    # Check if va's shard returns vb's expected value and vice versa
                    va_returns_vb = 0
                    vb_returns_va = 0
                    va_tests = 0
                    vb_tests = 0

                    def _find_golden_tuple(
                        oracle_name, failure_inputs, golden_tuples_list
                    ):
                        """Find the golden tuple matching this specific failure by
                        comparing input values (not just oracle name)."""
                        for o_var, fs in golden_tuples_list:
                            if o_var != oracle_name:
                                continue
                            # Check that all input keys/values from the failure
                            # match the golden tuple
                            match = True
                            for k, v_val in failure_inputs.items():
                                if k in fs and not self.are_similar(v_val, fs[k]):
                                    match = False
                                    break
                            if match:
                                return fs
                        return None

                    for f in failures.get(va, []):
                        if "got" in f and f.get("expected") is not None:
                            va_tests += 1
                            got_val = f["got"]
                            oracle = f["oracle"]
                            gt = _find_golden_tuple(
                                oracle, f.get("inputs", {}), golden_tuples
                            )
                            if gt and vb in gt:
                                if self.are_similar(got_val, gt[vb]):
                                    va_returns_vb += 1
                    for f in failures.get(vb, []):
                        if "got" in f and f.get("expected") is not None:
                            vb_tests += 1
                            got_val = f["got"]
                            oracle = f["oracle"]
                            gt = _find_golden_tuple(
                                oracle, f.get("inputs", {}), golden_tuples
                            )
                            if gt and va in gt:
                                if self.are_similar(got_val, gt[va]):
                                    vb_returns_va += 1
                    # If majority of tests show swapping, mark as swapped
                    if (
                        va_tests > 0
                        and va_returns_vb > va_tests * 0.5
                        and vb_tests > 0
                        and vb_returns_va > vb_tests * 0.5
                    ):
                        swapped_pairs.append((va, vb))
                        log_lines.append(
                            f" |- SWAP detected: {va} and {vb} appear to have swapped solvers"
                        )

        # Invariant vars get perfect scores
        for iv in invariant_vars:
            scores[iv] = num_v
            trusted.append(iv)

        # Build mismatches dict (compatible with existing pipeline format)
        mismatches = {}
        for v in broken:
            if failures[v]:
                # Convert to the format expected by analyze_results / repair
                mismatches[v] = []
                for f in failures[v][:3]:  # limit to 3 examples
                    entry = {"inputs": f["inputs"]}
                    if "expected" in f:
                        entry["output"] = f["expected"]
                        entry["mismatches"] = [
                            {
                                "target": v,
                                "expected": f["expected"],
                                "got": f.get("got"),
                            }
                        ]
                    if "error" in f:
                        entry["error"] = f["error"]
                    mismatches[v] = [entry]

        # Logging
        is_clean = not broken
        if self.verbose or not is_clean:
            status = "OK" if is_clean else "FAILED"
            if not is_clean:
                print(f"Testing {self.lib_class.__name__}::{base_eq}... {status}")
                for v in broken:
                    total = total_tests[v]
                    p = passes[v]
                    print(f"  {v}: {p}/{total} golden-tuple tests passed")
                    for f in failures[v][:2]:
                        if "error" in f:
                            print(f"    oracle={f['oracle']}: ERROR {f['error']}")
                        else:
                            print(
                                f"    oracle={f['oracle']}: expected={f['expected']}, got={f.get('got')}"
                            )
                for line in log_lines:
                    print(line)

        return {
            "scores": scores,
            "broken": broken,
            "trusted": trusted,
            "golden_tuples": [
                {"oracle": o, "values": v} for o, v in golden_tuples[:10]
            ],
            "failures": {v: failures[v][:5] for v in broken},
            "mismatches": mismatches,
            "swapped_pairs": swapped_pairs,
        }

    def verify(self):
        overall_results = {}
        for base_eq in self.base_equations:
            overall_results[base_eq] = self.verify_equation(base_eq)
        return overall_results

    def verify_golden(self, pinned_tuples_map=None):
        """Verify all equations using golden-tuple round-robin.

        *pinned_tuples_map* is an optional dict mapping ``base_eq`` to a list
        of ``(oracle_var, full_value_dict)`` tuples from a previous round.
        When provided, these tuples are reused for deterministic verification.
        """
        overall_results = {}
        for base_eq in self.base_equations:
            pinned = None
            if pinned_tuples_map and base_eq in pinned_tuples_map:
                pinned = pinned_tuples_map[base_eq]
            overall_results[base_eq] = self.verify_equation_golden(
                base_eq, pinned_tuples=pinned
            )
        return overall_results
